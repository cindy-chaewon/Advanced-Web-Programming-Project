-- ============================================================
-- 4-3) 식당 점수 — REVIEWS + SCRAPS에서 자동 집계 (시드 + 실데이터 모두 포함)
--      score_service.compute_total_score와 동일 공식: avg*14 + min(reviews,50)*0.4 + min(scraps,100)*0.1
-- ============================================================
INSERT INTO RESTAURANT_SCORES (restaurant_id, avg_review_score, review_count, scrap_count, total_score)
SELECT
    r.restaurant_id,
    COALESCE((SELECT ROUND(AVG(score), 2) FROM REVIEWS WHERE restaurant_id = r.restaurant_id AND score IS NOT NULL), 0),
    (SELECT COUNT(*) FROM REVIEWS WHERE restaurant_id = r.restaurant_id),
    (SELECT COUNT(*) FROM SCRAPS  WHERE restaurant_id = r.restaurant_id),
    LEAST(
        100,
        ROUND(
            COALESCE((SELECT AVG(score) FROM REVIEWS WHERE restaurant_id = r.restaurant_id AND score IS NOT NULL), 0) * 14
            + LEAST((SELECT COUNT(*) FROM REVIEWS WHERE restaurant_id = r.restaurant_id), 50) * 0.4
            + LEAST((SELECT COUNT(*) FROM SCRAPS  WHERE restaurant_id = r.restaurant_id), 100) * 0.1,
            1
        )
    )
FROM RESTAURANTS r
WHERE r.restaurant_id BETWEEN 100 AND 199
ON DUPLICATE KEY UPDATE
    avg_review_score = VALUES(avg_review_score),
    review_count     = VALUES(review_count),
    scrap_count      = VALUES(scrap_count),
    total_score      = VALUES(total_score);


